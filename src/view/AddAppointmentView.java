    private void btnBackupRestoreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackupRestoreActionPerformed
        // TODO add your handling code here:
        try {
            int c = JOptionPane.showConfirmDialog(null, "Do you want to restore the entire system?", "Confirmation", JOptionPane.YES_NO_CANCEL_OPTION);

            if (c == 0) {
                Statement st = DataBaseModel.createConnection().createStatement();
                ResultSet rst = st.executeQuery("SELECT * FROM appointment_tlb WHERE sno="+new LoginController().getSchedulleId()+" ORDER BY aid");
                while (rst.next()) {
                    listOne.insertEnding(rst.getInt(1), rst.getString(3), rst.getString(4), rst.getString(5), rst.getString(6), rst.getInt(7), rst.getString(8));
                }
                refresh();

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBackupRestoreActionPerformed

