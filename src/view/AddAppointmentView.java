    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        clearError();
        if (txtFirst.getText().equals("")) {
            lblErrorFirstName.setText("Cannot be null!");
            lblErrorFirstName.setVisible(true);
        }
        else if(txtFirst.getText().matches(".*\\d.*")){
            lblErrorFirstName.setText("Cannot have numbers!");
            lblErrorFirstName.setVisible(true);
        }
        else if(txtLast.getText().equals("")){
            lblErrorLastName.setText("Cannot be null!");
            lblErrorLastName.setVisible(true);
        }
        else if(txtLast.getText().matches(".*\\d.*")){
            lblErrorLastName.setText("Cannot have numbers!");
            lblErrorLastName.setVisible(true);
        }
        else if(txtNic.getText().equals("")){
            lblErrorNic.setText("Cannot be null!");
            lblErrorNic.setVisible(true);
        }
        else if(txtNic.getText().length()<10 || txtNic.getText().length()>12 || txtNic.getText().length()==11){
            lblErrorNic.setText("Incorrect nic nubmer!");
            lblErrorNic.setVisible(true);
        }
        else if(txtAddress.getText().equals("")){
            lblErrorAddress.setText("Cannot be null!");
            lblErrorAddress.setVisible(true);
        }
        else if(txtMobile.getText().equals("")){
            lblErrorMobile.setText("Cannot be null!");
            lblErrorMobile.setVisible(true);
        }
        else if(txtMobile.getText().length()!=10){
            lblErrorMobile.setText("Incorrect mobile number!");
            lblErrorMobile.setVisible(true);
        }
        else if(!(txtMobile.getText().matches("[0-9]+"))){
            lblErrorMobile.setText("Incorrect mobile number!");
            lblErrorMobile.setVisible(true);
        }
        else if(txtEmail.getText().equals("")){
            lblErrorEmail.setText("Cannot be null!");
            lblErrorEmail.setVisible(true);
        }
         else if(!(txtEmail.getText().matches("^[a-z0-9._%+-]+@[a-z0-9.-]+\\.[a-z]{3}$"))){
            lblErrorEmail.setText("Incorrect Email Address!");
            lblErrorEmail.setVisible(true);                
        }
        else {

            try {
                Statement st = DataBaseModel.createConnection().createStatement();
                int c = st.executeUpdate("INSERT into appointment_tlb (sno,fname,lname,nic,address,mobile,email) VALUES ("+new LoginController().getSchedulleId()+",'" + txtFirst.getText() + "','" + txtLast.getText() + "','" + txtNic.getText() + "','" + txtAddress.getText() + "'," + Integer.parseInt(txtMobile.getText()) + ",'" + txtEmail.getText() + "')");
                ResultSet rst = st.executeQuery("SELECT MAX(aid) FROM appointment_tlb");
                rst.next();
                if (c > 0) {
                    
                    listOne.insertEnding(rst.getInt(1), txtFirst.getText(), txtLast.getText(), txtNic.getText(), txtAddress.getText(), Integer.parseInt(txtMobile.getText()), txtEmail.getText());
                    refresh();
                    JOptionPane.showMessageDialog(null,"Data is recorded successfully","Information",JOptionPane.INFORMATION_MESSAGE);
                    clearTextBoxes();
                    clearError();
                }
                else{
                    JOptionPane.showMessageDialog(null,"Data is not recorded successfully!","Error!",JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage(),"Error!",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

